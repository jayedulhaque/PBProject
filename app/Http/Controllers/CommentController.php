     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $comment= new Comment;
        $comment->commentor_name=$request->get('commentor_name');
        $comment->commentor_designation=$request->get('commentor_designation');
        $comment->commentor_comments=$request->get('commentor_comments');
        $comment->approved_by='';
        $comment->approved_at='';
        $comment->save();
        return redirect('review')->with('success', 'Information has been added');
    }

